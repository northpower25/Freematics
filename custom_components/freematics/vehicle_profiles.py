def get_makes() -> list[str]:
    """Return sorted list of vehicle manufacturers."""
    return sorted(VEHICLE_DB.keys())


def get_models(make: str) -> list[str]:
    """Return sorted list of models for a given manufacturer."""
    return sorted(VEHICLE_DB.get(make, {}).keys())


def get_year_ranges(make: str, model: str) -> list[str]:
    """Return list of year-range labels for a given make/model."""
    entries = VEHICLE_DB.get(make, {}).get(model, [])
    return [label for label, _ in entries]


def get_vehicle_pids(make: str, model: str, year_range: str) -> str:
    """Return the extra PIDs CSV string for a specific make/model/year_range.

    Returns an empty string if the combination is not found or has no extra PIDs.
    """
    entries = VEHICLE_DB.get(make, {}).get(model, [])
    for label, pids in entries:
        if label == year_range:
            return pids
    return ""
